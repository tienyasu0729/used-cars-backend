package scu.dn.used_cars_backend.common.exception;

import lombok.Getter;
import org.springframework.http.HttpStatus;

@Getter
public enum ErrorCode {

	UNAUTHORIZED(HttpStatus.UNAUTHORIZED, "UNAUTHORIZED"),
	FORBIDDEN(HttpStatus.FORBIDDEN, "FORBIDDEN"),
	INVALID_CREDENTIALS(HttpStatus.UNAUTHORIZED, "INVALID_CREDENTIALS"),
	INVALID_CURRENT_PASSWORD(HttpStatus.BAD_REQUEST, "INVALID_CURRENT_PASSWORD"),
	PASSWORD_TOO_SHORT(HttpStatus.BAD_REQUEST, "PASSWORD_TOO_SHORT"),
	USER_NOT_FOUND(HttpStatus.NOT_FOUND, "USER_NOT_FOUND"),
	ACCOUNT_SUSPENDED(HttpStatus.FORBIDDEN, "ACCOUNT_SUSPENDED"),
	VALIDATION_FAILED(HttpStatus.BAD_REQUEST, "VALIDATION_FAILED"),
	INTERNAL_SERVER_ERROR(HttpStatus.INTERNAL_SERVER_ERROR, "INTERNAL_SERVER_ERROR"),

	VEHICLE_NOT_FOUND(HttpStatus.NOT_FOUND, "VEHICLE_NOT_FOUND"),
	BRAND_NOT_FOUND(HttpStatus.NOT_FOUND, "BRAND_NOT_FOUND"),
	MODEL_NOT_FOUND(HttpStatus.NOT_FOUND, "MODEL_NOT_FOUND"),
	BRANCH_NOT_FOUND(HttpStatus.NOT_FOUND, "BRANCH_NOT_FOUND"),
	INVALID_PRICE(HttpStatus.BAD_REQUEST, "INVALID_PRICE"),
	INVALID_YEAR(HttpStatus.BAD_REQUEST, "INVALID_YEAR"),
	LISTING_ID_CONFLICT(HttpStatus.CONFLICT, "LISTING_ID_CONFLICT"),
	VEHICLE_ALREADY_SAVED(HttpStatus.BAD_REQUEST, "VEHICLE_ALREADY_SAVED"),
	VEHICLE_NOT_SAVED(HttpStatus.NOT_FOUND, "VEHICLE_NOT_SAVED"),

	// Tier 3.2 — Booking & test drive
	BOOKING_NOT_FOUND(HttpStatus.NOT_FOUND, "BOOKING_NOT_FOUND"),
	SLOT_NOT_FOUND(HttpStatus.BAD_REQUEST, "SLOT_NOT_FOUND"),
	SLOT_FULLY_BOOKED(HttpStatus.BAD_REQUEST, "SLOT_FULLY_BOOKED"),
	VEHICLE_NOT_AVAILABLE(HttpStatus.BAD_REQUEST, "VEHICLE_NOT_AVAILABLE"),
	BOOKING_CANNOT_CANCEL(HttpStatus.BAD_REQUEST, "BOOKING_CANNOT_CANCEL"),
	INVALID_STATUS_TRANSITION(HttpStatus.BAD_REQUEST, "INVALID_STATUS_TRANSITION"),
	BOOKING_ACCESS_DENIED(HttpStatus.FORBIDDEN, "BOOKING_ACCESS_DENIED"),

	// Tier 3.3 — Vehicle transfers
	TRANSFER_NOT_FOUND(HttpStatus.NOT_FOUND, "TRANSFER_NOT_FOUND"),
	VEHICLE_NOT_IN_BRANCH(HttpStatus.BAD_REQUEST, "VEHICLE_NOT_IN_BRANCH"),
	TRANSFER_ALREADY_EXISTS(HttpStatus.BAD_REQUEST, "TRANSFER_ALREADY_EXISTS"),
	INVALID_TRANSFER_STATUS(HttpStatus.BAD_REQUEST, "INVALID_TRANSFER_STATUS"),
	TRANSFER_ACCESS_DENIED(HttpStatus.FORBIDDEN, "TRANSFER_ACCESS_DENIED");

	private final HttpStatus httpStatus;
	private final String code;

	ErrorCode(HttpStatus httpStatus, String code) {
		this.httpStatus = httpStatus;
		this.code = code;
	}
}
